// WHAT IS LCA?

// LCA of two nodes p and q is:

// The lowest node where both p and q are in its subtree

// Example
//         6
//        / \
//       2   8
//      / \ / \
//     0  4 7  9
//       / \
//      3   5

// LCA of 2 and 8:
// Answer = 6

// LCA of 3 and 5:
// Answer = 4

// WHY BST MAKES THIS EASY

// In normal binary tree → complex
// In BST → super simple because

